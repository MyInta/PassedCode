package inta02;

public class Validate {
	public static void main(String[] args){
		System.out.println("5>4的值是"+(5>4));
		System.out.println("5<4的值是"+(5<4));
		System.out.println("5>=4的值是"+(5>=4));
		System.out.println("5<=4的值是"+(5<=4));
		System.out.println("5==4的值是"+(5==4));
		System.out.println("5!=4的值是"+(5!=4));
	}

}
