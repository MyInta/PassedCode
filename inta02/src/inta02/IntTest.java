package inta02;

public class IntTest {
	public static void main(String[] args){
		//定义各类型变量
		byte m;
		short x;
		int i;
		long l;
		//为各类型变量赋值
		m=12;
		x=1;
		i=2;
		l=1000;
		//打印输出各类型变量的值
		System.out.println("byte类型变量m的值为："+m);
		System.out.println("short类型变量x的值为"+x);
		System.out.println("int类型变量i的值为"+i);
		System.out.println("long类型变量的值为"+l);
	}

}
