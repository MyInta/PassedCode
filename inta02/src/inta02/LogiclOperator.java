package inta02;

public class LogiclOperator {
	public static void main(String[] args){
		boolean x=true;
		boolean y=false;
		System.out.println("x&y的值是"+(x&y));
		System.out.println("x|y的值是"+(x|y));
		System.out.println("!x的值是"+(!x));
		System.out.println("x&&y的值是"+(x&&y));
		System.out.println("x||y的值是"+(x||y));
		System.out.println("x^y的值是"+(x^y));
		System.out.println("x^x的值是"+(x^x));
		System.out.println("x^!x的值是"+(x^!x));
	}

}
